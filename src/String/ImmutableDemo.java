package String;

public class ImmutableDemo {

    public static void main(String[] args) {
        String s1 = "Pune";
        String s2 = " Mumbai";
        String s3 = "codekul";
        System.out.println(s1.concat(",India"));

        System.out.println(s1);

        s1 = "Goa";
        System.out.println(s1);


        int s4 = 10;
        int s5 = 20;
        System.out.println(s4+s5);
    }

}
